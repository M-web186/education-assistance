# Education Assistance

A Python tool to assist learners in choosing the best study methods and making their work easier.

## Quick Start

```bash
# Clone the repository
git clone https://github.com/M-web186/education-assistance.git
cd education-assistance

# (Optional) Install dependencies
pip install -r requirements.txt

# Run the main program
python "AI learning plan.py"
```

